package veranstaltung_2;

public class SwitchCase {

	public static void main(String[] args) {
		String tag = "Montag";
		switch(tag) {
		case "Montag":
			System.out.println("Ich bin ein " + tag + ".");
		case "Dienstag":
			System.out.println("Ich bin ein " + tag + ".");
		case "Mittwoch":
			System.out.println("Ich bin ein " + tag + ".");
		case "Donnerstag":
			System.out.println("Ich bin ein " + tag + ".");
		case "Freitag":
			System.out.println("Ich bin ein " + tag + ".");
		case "Samstag":
			System.out.println("Ich bin ein " + tag + ".");
		case "Sonntag":
			System.out.println("Ich bin ein " + tag + ".");
		}
	}

}
