package veranstaltung_2.uebungen;

public class Uebung2 {

	public static void main(String[] args) {
		String tag = "Montag";
		switch(tag) {
		case "Montag":
			System.out.println("Ich bin ein " + tag + ".");
			break;
		case "Dienstag":
			System.out.println("Ich bin ein " + tag + ".");
			break;
		case "Mittwoch":
			System.out.println("Ich bin ein " + tag + ".");
			break;
		case "Donnerstag":
			System.out.println("Ich bin ein " + tag + ".");
			break;
		case "Freitag":
			System.out.println("Ich bin ein " + tag + ".");
			break;
		case "Samstag":
			System.out.println("Ich bin ein " + tag + ".");
			break;
		case "Sonntag":
			System.out.println("Ich bin ein " + tag + ".");
			break;
		}

	}

}
